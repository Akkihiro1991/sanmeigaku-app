export function buildPersonDescription(yosen: Yosen): string {
  const base = CHUO_SEI_DESC[yosen.chuo.sei] ?? '';
  const suffix = SHINKYO_SUFFIX[yosen.shinkyoBun] ?? '';
  return base + (suffix ? '　' + suffix : '');
}


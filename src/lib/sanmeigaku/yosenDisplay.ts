/** 各宮から「帰星」表示用に主星を選ぶ（牽牛星があればそれを優先） */
function pickKiseiStar(y: Yosen): string {
  const bags: SeiBag[] = [
    y.chuo,
    y.kita,
    y.minami,
    y.higashi,
    y.nishi,
    y.kitahigashi,
    y.kitanishi,
    y.minamihigashi,
    y.minamishi,
    y.oku,
  ];
  const hit = bags.find((b) => b.sei === '牽牛星');
  return hit?.sei ?? y.kita.sei;
}

